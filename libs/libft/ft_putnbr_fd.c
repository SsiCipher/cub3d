#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	num;

	if (n < 0)
	{
		if (n == -2147483648)
			write(fd, "-2147483648", 11);
		else
		{
			write(fd, "-", 1);
			ft_putnbr_fd(n * -1, fd);
		}
	}
	else
	{
		if (n >= 10)
			ft_putnbr_fd(n / 10, fd);
		num = n % 10 + '0';
		write(fd, &num, 1);
	}
}
